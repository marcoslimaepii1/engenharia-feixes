    private void commandTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_commandTextActionPerformed
        final String host = hostText.getText();
        final int port = Parse.toInt(portText.getText());
        final String command = commandText.getText();

        try {

            // Get the internet address of the specified host
            final InetAddress address = InetAddress.getByName(host);
            // Initialize a datagram packet with data and address
            final byte[] data = command.getBytes();
            DatagramPacket packet = new DatagramPacket(data, data.length, address, port);
            // Create a datagram socket, send the packet through it, close it.
            DatagramSocket dsocket = new DatagramSocket();
            dsocket.send(packet);
            dsocket.close();
        } catch (UnknownHostException ex) {
            Logger.getLogger(UDPClient.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UDPClient.class.getName()).log(Level.SEVERE, null, ex);
        }

        commandText.setText("");

    }//GEN-LAST:event_commandTextActionPerformed

