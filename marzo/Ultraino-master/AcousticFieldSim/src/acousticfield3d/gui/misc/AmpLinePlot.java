    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        final Entity ent = mf.movePanel.getBeadEntity();
        if(ent == null) {return;}
        
        final StringBuilder sb = new StringBuilder();
        final float length = Parse.toFloat( lengthText.getText() );
        final Vector3f dir = new Vector3f( directionText.getText() );
        final int steps = Parse.toInt( stepsText.getText() );
        final boolean center = centerCheck.isSelected();
        
        final float stepSize = length/steps;
        dir.normalizeLocal();
        
        final Vector3f pos = ent.getTransform().getTranslation().clone();
        final Vector3f cPos = pos.clone().subtractLocal( dir.mult(length/2) );
        for (int i = 0; i < steps; i++) {
            final float amp = CalcField.calcFieldAt(cPos, mf).length();
            sb.append(cPos.toStringSimple("\t") + "\t" + amp + "\n");
            cPos.addLocalInc(dir, stepSize);
        }
        
        TextFrame.showText("Amplitude Line", sb.toString(), this);
    }//GEN-LAST:event_jButton1ActionPerformed


